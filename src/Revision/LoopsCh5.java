package Revision;

public class LoopsCh5 {
    public static void main(String[] args) {
        //Loops
        //Three types of loops
        //While loop
        System.out.println("Ascending order");
        int a = 1;
        while (a <= 10){
            System.out.println(a);
            a++;
        }
        System.out.println(" ");
        System.out.println("Descending order");
        int b = 20;
        while (b>=1){
            System.out.println(b);
            b--;
        }
    }
}
